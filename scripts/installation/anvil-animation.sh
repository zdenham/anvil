play_anvil_animation() {
  # Skip animation for narrow terminals
  local cols
  cols=$(tput cols 2>/dev/null || echo 80)
  if [ "$cols" -lt 80 ]; then
    printf '%s\n' "$ANVIL_LOGO"
    return 1
  fi

  tput civis 2>/dev/null || true

  local frame_idx=0
  while true; do
    # Move cursor to top of animation area
    printf '\033[%dA' "$ANVIL_FRAME_HEIGHT" 2>/dev/null || true

    # Print current frame
    local var="ANVIL_FRAME_${frame_idx}"
    printf '%s\n' "${!var}"

    frame_idx=$(( (frame_idx + 1) % ANVIL_FRAME_COUNT ))
    sleep 0.25
  done
}

stop_anvil_animation() {
  local pid="$1"
  kill "$pid" 2>/dev/null || true
  wait "$pid" 2>/dev/null || true

  # Move to top of animation area and clear it
  printf '\033[%dA' "$ANVIL_FRAME_HEIGHT" 2>/dev/null || true
  for i in $(seq 1 "$ANVIL_FRAME_HEIGHT"); do
    printf '\033[2K\n'
  done
  printf '\033[%dA' "$ANVIL_FRAME_HEIGHT" 2>/dev/null || true

  # Print anvil-only logo
  printf '%s\n' "$ANVIL_LOGO"

  tput cnorm 2>/dev/null || true
}

init_anvil_animation() {
  # Print blank lines to reserve space for the animation
  for i in $(seq 1 "$ANVIL_FRAME_HEIGHT"); do
    printf '\n'
  done
}
